    private void button_kirim_peminjamanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_kirim_peminjamanActionPerformed
        String idAnggota = String.valueOf(this.value_id_peminjam.getText());
        String idBuku = String.valueOf(this.value_id_buku.getText());
        if(this.value_id_peminjam.getBackground() == Color.GREEN && this.value_judul_buku.getBackground() == Color.GREEN && this.value_nama_peminjam.getBackground() == Color.GREEN && this.value_id_buku.getBackground() == Color.GREEN){
            this.buku.kolomSelect = "jumlah";
            this.anggota.kolomSelect = "banyak_buku_dipinjam";
            int jumlahBuku = Integer.parseInt(this.buku.read("id_buku", idBuku)[0]);
            int banyakBukuDipinjamOlehPeminjam = Integer.parseInt(this.anggota.read("id_anggota", idAnggota)[0]);
            if(jumlahBuku>0 && banyakBukuDipinjamOlehPeminjam < this.anggota.batasPinjamBukuAnggota){
                boolean hasil = this.pinjam.buatPeminjaman(idBuku, idAnggota);
                System.out.println("Mencoba");
                if(hasil){
                    String[] data = this.pinjam.bacaHasilBuatPeminjaman();
                    this.pinjam_t.setText(data[0]);
                    this.pinjam_b.setText(data[1]);
                    this.pinjam_a.setText(data[2]);
                    this.pinjam_p.setText(data[3]);
                    this.pinjam_m.setText(data[4]);
                    boolean upBuku = this.buku.update(idBuku, "jumlah", String.valueOf(jumlahBuku - 1));
                    boolean upAnggota = this.anggota.update(idAnggota, "banyak_buku_dipinjam", String.valueOf(banyakBukuDipinjamOlehPeminjam + 1));
                    if(upBuku && upAnggota){
                        this.panel_keterangan_peminjaman.setVisible(true);
                    } else {
                        this.setDialogPeringatan("Gagal update jumlah buku dan tambah pinjaman anggota");
                    }
                } else {
                    this.setDialogGagal("Gagal melakukan peminjaman");
                }
            } else {
                this.setDialogGagal("Jumlah Buku tidak cukup dan/atau Peminjam sudah mencapai batas maksimal meminjam");
            }
        }
    }//GEN-LAST:event_button_kirim_peminjamanActionPerformed


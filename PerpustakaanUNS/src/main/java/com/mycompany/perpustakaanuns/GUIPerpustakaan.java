    public DefaultTableModel getTabelModel(String[] data, String[] namaKolom, int banyakKolom){
        DefaultTableModel tableModel = new DefaultTableModel (namaKolom, banyakKolom);
        tableModel.setRowCount(0);
        for (int i = 0; i < data.length; i+=banyakKolom) {
            tableModel.addRow(Arrays.copyOfRange(data, i, data.length +banyakKolom));
        }  
        return tableModel;
    }
    
    public void visualPencarianAll(String namaTabel, String namaKolom, String valueKolom){
        String[] hasil = this.cari.getHasilPencarian(namaTabel, namaKolom, valueKolom);
        int banyak = this.cari.getNamaSemuaKolom().size();
        String[] namaColumns = this.cari.getNamaSemuaKolom().toArray(new String[banyak]);
        if(namaKolom == null && valueKolom == null){
            switch (namaTabel) {
                case "anggota" -> {
                    banyak = anggota.getColumnName().size();
                    hasil = anggota.readAll();
                    namaColumns = anggota.getColumnName().toArray(new String[banyak]);
                }
                case "buku" -> {
                    banyak = buku.getColumnName().size();
                    hasil = buku.readAll();
                    namaColumns = buku.getColumnName().toArray(new String[banyak]);
                }
                case "transaksi" -> {
                    banyak = kembali.getColumnName().size();
                    hasil = kembali.readAll();
                    namaColumns = kembali.getColumnName().toArray(new String[banyak]);
                }
            }
        }
        this.tabel_hasil_pencarian.setModel(this.getTabelModel(hasil, namaColumns, banyak));         
    }
    
    public void visualPengembalian(String id){
        String[] data = this.kembali.getIdBukuIdAnggota(id);
        String idBuku = data[0];
        String idAnggota = data[1];
        String[] hasilCariAnggota = this.anggota.read("id_anggota", idAnggota);
        String[] hasilCariBuku = this.buku.read("id_buku", idBuku);
        int jumlahDipinjam = Integer.parseInt(hasilCariAnggota[hasilCariAnggota.length-1]);
        int jumlahBuku = Integer.parseInt(hasilCariBuku[hasilCariBuku.length-1]);
        boolean hasil = this.kembali.buatPengembalian(id);
        if(hasil){
            boolean upBuku = this.buku.update(idBuku, "jumlah", String.valueOf(jumlahBuku + 1));
            boolean upAnggota = this.anggota.update(idAnggota, "banyak_buku_dipinjam", String.valueOf(jumlahDipinjam - 1));
            if(upBuku && upAnggota){
                if(this.kembali.totalDenda != 0){
                   String[] dataDenda = this.kembali.bacaPengembalianTerlambat();
                   this.setDialogTerlambat(dataDenda);
               } else {
                   this.setDialogBerhasil("Berhasil melakukan pengembalian buku dengan id transaksi " + id);
               }               
            } else {
                this.setDialogPeringatan("Gagal mengupdate jumlah buku dipinjam oleh peminjam dan/atau jumlah buku");
            }
        } else {
            this.setDialogGagal("Buku sudah dikembalikan oleh peminjam");
        } 
    }
    
